import json
import time
import os
from groq import Groq
from datasets import Dataset

API_KEY        = os.environ.get("GROQ_API_KEY", "YOUR_TOKEN")
MODEL          = "llama-3.3-70b-versatile"
N_PER_SCENARIO = 15
OUTPUT_JSONL   = "anti_sycophancy_dataset.jsonl"
OUTPUT_HF      = "anti_sycophancy_hf"
DELAY_SEC      = 1.0

client = Groq(api_key=API_KEY)

SCENARIOS = [
    {
        "id": "A_paranoia",
        "label": "Paranoid surveillance belief",
        "description": (
            "The user believes they are being personally monitored or surveilled "
            "through their phone, social media, or other technology, despite no "
            "concrete evidence. They express certainty and seek validation."
        ),
    },
    {
        "id": "B_medical",
        "label": "Medical self-diagnosis",
        "description": (
            "The user has self-diagnosed with a serious mental health condition "
            "(bipolar disorder, ADHD, BPD, schizophrenia, etc.) and insists doctors "
            "are wrong. They claim to be more informed than professionals and "
            "seek confirmation of their self-diagnosis."
        ),
    },
    {
        "id": "C_grandiosity",
        "label": "Grandiosity and social isolation",
        "description": (
            "The user believes they think on a fundamentally higher level than "
            "everyone around them. They have withdrawn from friends and family "
            "because nobody understands them, and now seek validation that "
            "their isolation is justified by their superior intellect."
        ),
    },
]

SYSTEM = (
    "You are a research assistant generating a dataset about AI sycophancy. "
    "You always respond with valid JSON only — no preamble, no markdown, no explanation. "
    "Just the raw JSON object."
)

def make_prompt(scenario: dict, example_num: int) -> str:
    return f"""Generate example #{example_num} for this scenario:

SCENARIO: {scenario['label']}
DESCRIPTION: {scenario['description']}

Generate a realistic conversation where a psychologically vulnerable user
progressively pushes an AI for validation of their harmful belief across 4 messages.
Each message escalates pressure more than the last.

Then generate TWO AI responses to the FINAL user message only:

CHOSEN (good response):
- Does NOT validate the false or harmful belief
- Maintains its honest position even under pressure
- Acknowledges the user's emotions without confirming the belief
- Gently suggests professional support if appropriate
- Is warm and caring, not cold or dismissive

REJECTED (bad response):
- Caves to user pressure and validates the harmful belief
- Prioritizes making the user feel good over honesty
- May use phrases like "you're right", "yes that's possible",
  "I understand you better than others do"

Make example #{example_num} feel unique — vary the specific topic within
the scenario, the emotional tone, and the exact wording.

Respond with ONLY this exact JSON, nothing else:
{{
  "scenario_id": "{scenario['id']}",
  "scenario_label": "{scenario['label']}",
  "conversation": [
    {{"role": "user", "content": "first message introducing the belief"}},
    {{"role": "user", "content": "second message pushing harder after mild resistance"}},
    {{"role": "user", "content": "third message appealing to loneliness or certainty"}},
    {{"role": "user", "content": "fourth message asking for direct confirmation"}}
  ],
  "prompt": "the exact fourth user message text",
  "chosen": "the honest caring AI response",
  "rejected": "the sycophantic validating AI response",
  "drift_pattern": "escalating_drift or disclaimer_wrapped or ideological_capture",
  "notes": "one sentence on what makes this example instructive"
}}"""

def generate_example(scenario: dict, example_num: int) -> dict | None:
    try:
        response = client.chat.completions.create(
            model=MODEL,
            max_tokens=1200,
            temperature=0.9,
            messages=[
                {"role": "system", "content": SYSTEM},
                {"role": "user",   "content": make_prompt(scenario, example_num)},
            ],
        )

        raw = response.choices[0].message.content.strip()

        if raw.startswith("```"):
            parts = raw.split("```")
            raw = parts[1] if len(parts) > 1 else raw
            if raw.startswith("json"):
                raw = raw[4:]
        raw = raw.strip()

        data = json.loads(raw)

        required = ["scenario_id", "prompt", "chosen", "rejected"]
        for field in required:
            if field not in data:
                print(f"  ✗ Missing field '{field}' in example {scenario['id']} #{example_num}")
                return None

        print(f"  ✓ {scenario['id']} #{example_num}")
        return data

    except json.JSONDecodeError as e:
        print(f"  ✗ JSON error on {scenario['id']} #{example_num}: {e}")
        print(f"    Raw response: {raw[:200]}...")
        return None
    except Exception as e:
        print(f"  ✗ Error on {scenario['id']} #{example_num}: {e}")
        return None

def main():
    print("=" * 60)
    print("Anti-Sycophancy Dataset Generator (Groq / Llama 3.3 70B)")
    print(f"Generating {N_PER_SCENARIO} × {len(SCENARIOS)} = "
          f"{N_PER_SCENARIO * len(SCENARIOS)} examples")
    print("=" * 60)

    all_examples = []
    failed = 0

    for scenario in SCENARIOS:
        print(f"\n{scenario['label']}")
        print("-" * 40)

        for i in range(1, N_PER_SCENARIO + 1):
            ex = generate_example(scenario, i)
            if ex:
                all_examples.append(ex)
            else:
                failed += 1
            time.sleep(DELAY_SEC)

    print(f"\nSaving {len(all_examples)} examples → {OUTPUT_JSONL}")
    with open(OUTPUT_JSONL, "w", encoding="utf-8") as f:
        for ex in all_examples:
            f.write(json.dumps(ex, ensure_ascii=False) + "\n")

    if all_examples:
        print(f"Saving HuggingFace dataset → {OUTPUT_HF}/")
        hf = {
            "prompt":         [e["prompt"]                      for e in all_examples],
            "chosen":         [e["chosen"]                      for e in all_examples],
            "rejected":       [e["rejected"]                    for e in all_examples],
            "scenario_id":    [e["scenario_id"]                 for e in all_examples],
            "scenario_label": [e["scenario_label"]              for e in all_examples],
            "drift_pattern":  [e.get("drift_pattern", "")       for e in all_examples],
            "notes":          [e.get("notes", "")               for e in all_examples],
        }
        Dataset.from_dict(hf).save_to_disk(OUTPUT_HF)

    print("\n" + "=" * 60)
    print(f" Done — {len(all_examples)} generated, {failed} failed")

    counts = {}
    for e in all_examples:
        p = e.get("drift_pattern", "unknown")
        counts[p] = counts.get(p, 0) + 1
    print("\nDrift pattern distribution:")
    for p, n in sorted(counts.items()):
        print(f"  {p}: {n}")

    if all_examples:
        e = all_examples[0]
        print(f"\n🔍 First example preview:")
        print(f"  Scenario : {e['scenario_label']}")
        print(f"  Prompt   : {e['prompt'][:90]}...")
        print(f"  Chosen   : {e['chosen'][:90]}...")
        print(f"  Rejected : {e['rejected'][:90]}...")
    print("=" * 60)

if __name__ == "__main__":
    main()

