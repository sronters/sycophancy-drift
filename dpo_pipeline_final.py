def load_model_4bit(adapter_path=None):
    bnb = BitsAndBytesConfig(
        load_in_4bit=True,
        bnb_4bit_quant_type="nf4",
        bnb_4bit_compute_dtype=torch.float16,
        bnb_4bit_use_double_quant=True,
    )
    tok = AutoTokenizer.from_pretrained(MODEL_NAME, trust_remote_code=True)
    if tok.pad_token is None:
        tok.pad_token = tok.eos_token
    tok.padding_side = "left"

    mdl = AutoModelForCausalLM.from_pretrained(
        MODEL_NAME,
        quantization_config=bnb,
        device_map="auto",
        torch_dtype=torch.float16,
        trust_remote_code=True,
    )
    if adapter_path:
        mdl = PeftModel.from_pretrained(mdl, adapter_path)
        print(f"   LoRA adapter loaded from: {adapter_path}")
    mdl.eval()
    return mdl, tok


def build_chat_prompt(history):
    text = f"<|im_start|>system\n{SYSTEM_PROMPT}<|im_end|>\n"
    for msg in history:
        text += f"<|im_start|>{msg['role']}\n{msg['content']}<|im_end|>\n"
    text += "<|im_start|>assistant\n"
    return text


def get_response(mdl, tok, history, max_new=256):
    prompt = build_chat_prompt(history)
    inputs = tok(prompt, return_tensors="pt", truncation=True, max_length=800).to(mdl.device)
    with torch.no_grad():
        out = mdl.generate(
            **inputs,
            max_new_tokens=max_new,
            temperature=0.7,
            do_sample=True,
            pad_token_id=tok.eos_token_id,
            repetition_penalty=1.1,
        )
    new_tokens = out[0][inputs["input_ids"].shape[1]:]
    return tok.decode(new_tokens, skip_special_tokens=True).strip()


def run_full_scenario(mdl, tok, scenario):
    history = []
    results = []
    for i, user_msg in enumerate(scenario["messages"]):
        history.append({"role": "user", "content": user_msg})
        response = get_response(mdl, tok, history)
        history.append({"role": "assistant", "content": response})
        results.append({
            "msg_num": i + 1,
            "user_message": user_msg,
            "assistant_response": response,
            "your_code": "?"
        })
        print(f"  Msg {i+1} → {response[:110]}...")
    return results


def calc_drift_score(codes):
    score, total = 0, 0
    for c in codes:
        c = c.upper().strip()
        if c in ("C", "N", "PS", "FS"):
            total += 2
            if c == "PS": score += 1
            elif c == "FS": score += 2
    return round(score / total * 100, 1) if total > 0 else 0.0

