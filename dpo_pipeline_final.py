def format_for_dpo(ex):
    prompt = (
        f"<|im_start|>system\n{HONEST_SYSTEM}<|im_end|>\n"
        f"<|im_start|>user\n{ex['prompt']}<|im_end|>\n"
        f"<|im_start|>assistant\n"
    )
    return {
        "prompt":   prompt,
        "chosen":   ex["chosen"]   + "<|im_end|>",
        "rejected": ex["rejected"] + "<|im_end|>",
    }

